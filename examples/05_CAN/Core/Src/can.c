/**
  * @brief Check CAN1 and CAN2 with loopback cable
  * @retval None
  */
HAL_StatusTypeDef MX_CAN_Loopback_Check(void)
{
  HAL_StatusTypeDef ret = HAL_ERROR;

  CAN_TxHeaderTypeDef   TxHeader;
  CAN_RxHeaderTypeDef   RxHeader;
  uint8_t               TxData[8];
  uint8_t               RxData[8];
  uint32_t              TxMailbox;

  CAN_FilterTypeDef  sFilterConfig;

  /*##-2- Configure the CAN Filter ###########################################*/
  sFilterConfig.FilterBank = 0;
  sFilterConfig.FilterMode = CAN_FILTERMODE_IDMASK;
  sFilterConfig.FilterScale = CAN_FILTERSCALE_32BIT;
  sFilterConfig.FilterIdHigh = 0x0000;
  sFilterConfig.FilterIdLow = 0x0000;
  sFilterConfig.FilterMaskIdHigh = 0x0000;
  sFilterConfig.FilterMaskIdLow = 0x0000;
  sFilterConfig.FilterActivation = ENABLE;
  sFilterConfig.SlaveStartFilterBank = 14;

  sFilterConfig.FilterFIFOAssignment = CAN_RX_FIFO0;
  ret = HAL_CAN_ConfigFilter(&hcan1, &sFilterConfig);
  if(ret != HAL_OK)
  {
    printf("CAN1 Filter failed\r\n");
    return ret;
  }

  /*##-3- Start the CAN peripheral ###########################################*/
  ret = HAL_CAN_Start(&hcan1);
  if (ret != HAL_OK)
  {
    printf("CAN1 Start failed\r\n");
    return ret;
  }

  ret = HAL_CAN_Start(&hcan2);
  if (ret != HAL_OK)
  {
    printf("CAN2 Start failed\r\n");
    return ret;
  }

  /*##-4- Start the Transmission process #####################################*/
  TxHeader.StdId = 0x123;
  TxHeader.RTR = CAN_RTR_DATA;
  TxHeader.IDE = CAN_ID_STD;
  TxHeader.DLC = 2;
  TxHeader.TransmitGlobalTime = DISABLE;
  TxData[0] = 0xDE;
  TxData[1] = 0xAD;

  /* Request transmission */
  ret = HAL_CAN_AddTxMessage(&hcan2, &TxHeader, TxData, &TxMailbox);
  if (ret != HAL_OK)
  {
    printf("CAN2 Queue TX failed\r\n");
    return ret;
  }

  /* Wait transmission complete */
  while(HAL_CAN_GetTxMailboxesFreeLevel(&hcan2) != 3) {}

  /* Check there's a message waiting */
  if (HAL_CAN_GetRxFifoFillLevel(&hcan1, CAN_RX_FIFO0) != 1)
  {
    printf("CAN1 RX FIFO failed\r\n");
    return HAL_ERROR;
  }

  /* Read the message */
  ret = HAL_CAN_GetRxMessage(&hcan1, CAN_RX_FIFO0, &RxHeader, RxData);
  if (ret != HAL_OK)
  {
    printf("CAN1 RX failed\r\n");
    return ret;
  }

  /* Check that it matches */
  if((RxHeader.StdId != 0x123)                     ||
     (RxHeader.RTR != CAN_RTR_DATA)               ||
     (RxHeader.IDE != CAN_ID_STD)                 ||
     (RxHeader.DLC != 2)                          ||
     ((RxData[0]<<8 | RxData[1]) != 0xDEAD))
  {
    /* Rx message Error */
    printf("CAN Validation failed\r\n");
    ret = HAL_ERROR;
  }

  printf("CAN Loopback Success!\r\n");

  return ret;
}


